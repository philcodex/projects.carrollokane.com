# demo.py
# A simple script to show that Python is working on your Mac.

# 1. Print a message
print("Python is working on your Mac!")

# 2. Create a variable
name = "Phil"
print("Hello,", name)

# 3. A simple function
def add_numbers(a, b):
    return a + b

# 4. Use the function
result = add_numbers(5, 7)
print("5 + 7 =", result)

# 5. A basic conditional
if result > 10:
    print("The result is greater than 10.")
else:
    print("The result is 10 or less.")

# 6. A loop
print("Counting to 5:")
for i in range(1, 6):
    print(i)

print("Done!")
